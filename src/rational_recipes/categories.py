"""Static title → category mapping for catalog variants (vwt.33).

The pipeline writes one ``variants`` row per dish; the ``category`` column
backs the PWA's category dropdown. Source for the mapping is the
L1-normalized title (e.g. ``"chicken pot pie"``, ``"pumpkin bread"``).
``categorize`` walks an ordered keyword-rule list and returns the first
hit, or ``None`` if no rule matches.

Static mapping over an LLM call by design: 38k variants × one prompt is
expensive and non-deterministic, while a 250-line rule table covers the
vast majority of L1 titles correctly and is trivially reviewable. When
new L1 groups appear that aren't covered, add a keyword rather than
threading another LLM step through the pipeline.

Rule ordering matters. High-specificity overrides come first
(``"bread pudding"`` → dessert, ahead of plain ``"bread"`` → bread),
then soup/salad, then main-dish phrases (``"pot pie"`` ahead of
``"pie"``), sauces/condiments, appetizers, bread, breakfast, dessert,
and finally side dishes as the loosest catch-all.
"""

