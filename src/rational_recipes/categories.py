def categorize(normalized_title: str) -> str | None:
    """Return a category label for an L1-normalized recipe title.

    ``None`` when no rule matches — callers writing the DB can pass that
    through as NULL and the PWA will render the "uncategorized" UI label.
    """
    if not normalized_title:
        return None
    title = normalized_title.lower()
    for category, keywords in _RULES:
        for kw in keywords:
            pattern = rf"\b{re.escape(kw)}\b"
            if re.search(pattern, title):
                return category
    return None
